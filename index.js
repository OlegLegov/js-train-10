/**
 * Функція `customAssign` об'єднує властивості декількох об'єктів за допомогою методу `assign`.
 * Крім того, вона перетворює значення числових властивостей на рядки і замінює пробіли на підкреслення.
 * objects - Об'єкти, властивості яких будуть об'єднані.
 * Повертаємо - Новий об'єкт з об'єднаними властивостями.
 */
function customAssign(...objects) {
  // Перевірка, чи передано принаймні два об'єкти
  if (objects.length < 2) {
    return "Помилка: Має бути передано принаймні два об'єкти.";
  }

  // Об'єкт, в який будуть об'єднані властивості
  let mergedObject = {};

  // Ітерація через кожен об'єкт у списку
  for (let obj of objects) {
    // Перевірка, чи obj є об'єктом
    if (typeof obj !== "object" || obj === null) {
      continue; // Якщо obj не є об'єктом, перейти до наступного об'єкту
    }

    // Об'єднання властивостей об'єкта зі змінною mergedObject за допомогою методу assign
    Object.assign(mergedObject, obj);
  }

  // Ітерація через кожну властивість новоствореного об'єкта
  for (let key in mergedObject) {
    // Перевірка, чи значення властивості є числом
    if (typeof mergedObject[key] === "number") {
      // Конвертування числа в рядок та заміна пробілів на підкреслення
      mergedObject[key] = mergedObject[key].toString().replace(/\s/g, "_");
    }
  }

  // Повернення нового об'єкта з об'єднаними та зміненими властивостями
  return mergedObject;
}

// Приклад використання функції customAssign
